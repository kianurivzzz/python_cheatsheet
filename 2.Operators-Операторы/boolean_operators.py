'''
-----------------------------------------------RU----------------------------------------------------------------
Логические операторы в Python те же, что и в математике.

< -> меньше
> -> больше
<= -> меньше или равно
>= -> больше или равно
== -> равно
!= -> не равно

------------------------------------------------EN---------------------------------------------------------------
The logical operators in Python are the same as those in math.

< -> less
> -> more
<= -> less or equal
>= -> more or equal
== -> equal
!= -> not equal

'''

number_1 = 27
number_2 = 5

print(number_1 < number_2) # False
print(number_1 > number_2) # True
print(number_1 <= number_2) # False
print(number_1 >= number_2) # True
print(number_1 == number_2) # False
print(number_1 != number_2) # True

# print() - название функции. Названия функций обязательно записываются со скобками/function name.
