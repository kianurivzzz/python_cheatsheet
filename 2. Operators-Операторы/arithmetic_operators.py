'''
-----------------------------------------------RU----------------------------------------------------------------
Арифметические операторы в Python те же, что и в математике.

+ -> сложение
- -> вычетание
* -> умножение
** -> возведение в степень
/ -> деление
// -> целочисленное деление
% -> деление с остатком

------------------------------------------------EN---------------------------------------------------------------
The arithmetic operators in Python are the same as those in math.

+ -> addition
-> subtraction
* -> multiplication
** -> exponentiation
/ -> division
// -> integer division
% -> division with a remainder

'''

number_1 = 27
number_2 = 5

print(number_1 + number_2) # 32
print(number_1 - number_2) # 22
print(number_1 * number_2) # 135
print(number_1 ** number_2) # 14348907
print(number_1 / number_2) # 5.4
print(number_1 // number_2) # 5
print(number_1 % number_2) # 2

# print() - название функции. Названия функций обязательно записываются со скобками/function name.
#Function names must be written with parentheses

